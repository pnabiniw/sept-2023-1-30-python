# Numbers in python are immutable type
# They are further classified as integer, float and complex


# Integer Type
a = 5
b = 30
c = -10
d = -20

# Float type
a = 5.2
b = 1.0
c = -2.3
d = 2e3   # 2000
e = 2e-4  # 0.0002


# Complex type
a = 2 + 3j
b = 3 - 2j
c = 4j


# We can check the "type" of an object using type() built-in function
a = 2j
print(type(a))   # complex
b = 30
print(type(b))  # integer
