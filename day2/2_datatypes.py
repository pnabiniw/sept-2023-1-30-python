############# Mutable and Immutable Objects ######################
# Those objects which can be changed after their creation they are called mutable objects
# Those objects which can't be changes after their creation are called immutable objects
# Numbers, boolean, string and tuple are immutable types in python
# List, set and dictionary are the mutable types in python

