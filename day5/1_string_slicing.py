data = "Hello World. I am learning python"

# String slicing is similar to the list slicing
print(data[:6])  # 'Hello '
print(data[4:])  # 'o World. I am learning python'
print(data[0:4])  # 'Hell'
print(data[2:8])  # 'llo Wo'
print(data[9:3])  # ''
print(data[-2:-8])  # ''
print(data[-9:-2])  # 'ng pyth'
print(data[-8:7])  # ''
print(data[2:9:2])  # 'loWr'

